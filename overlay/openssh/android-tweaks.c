/*
 * crypt() - password hashing for Android.
 *
 * Android bionic libc does not provide crypt(), so OpenSSH xcrypt.c
 * falls back to #define crypt DES_crypt, which only handles traditional
 * DES hashes. This custom crypt() supports $1$ (MD5-crypt) using
 * OpenSSL's MD5 API, so password authentication works with MD5 hashes
 * generated by "openssl passwd -1".
 *
 * Falls back to DES_crypt() for non-MD5 salts.
 */

static const char itoa64[] =
    "./0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";

static void
to64(char *s, unsigned long v, int n)
{
	while (--n >= 0) {
		*s++ = itoa64[v & 0x3f];
		v >>= 6;
	}
}

/*
 * MD5-crypt implementation (FreeBSD/Poul-Henning Kamp algorithm).
 * Compatible with crypt(3) $1$ format used by "openssl passwd -1".
 */
static char *
crypt_md5(const char *password, const char *salt)
{
	MD5_CTX ctx, ctx1;
	unsigned char digest[MD5_DIGEST_LENGTH];
	unsigned char alt_result[MD5_DIGEST_LENGTH];
	static char result[128];
	int password_len = strlen(password);
	int salt_len, i, pl;
	const char *sp;
	char *cp;

	sp = salt + 3;
	for (salt_len = 0; salt_len < 8 && *sp && *sp != '$'; sp++)
		salt_len++;

	MD5_Init(&ctx);
	MD5_Update(&ctx, password, password_len);
	MD5_Update(&ctx, "$1$", 3);
	MD5_Update(&ctx, salt + 3, salt_len);

	MD5_Init(&ctx1);
	MD5_Update(&ctx1, password, password_len);
	MD5_Update(&ctx1, salt + 3, salt_len);
	MD5_Update(&ctx1, password, password_len);
	MD5_Final(alt_result, &ctx1);

	for (pl = password_len; pl > MD5_DIGEST_LENGTH;
	     pl -= MD5_DIGEST_LENGTH)
		MD5_Update(&ctx, alt_result, MD5_DIGEST_LENGTH);
	MD5_Update(&ctx, alt_result, pl);

	memset(alt_result, 0, MD5_DIGEST_LENGTH);

	for (i = password_len; i != 0; i >>= 1) {
		if (i & 1)
			MD5_Update(&ctx, alt_result, 1);
		else
			MD5_Update(&ctx, password, 1);
	}

	MD5_Final(digest, &ctx);

	for (i = 0; i < 1000; i++) {
		MD5_Init(&ctx);

		if (i & 1)
			MD5_Update(&ctx, password, password_len);
		else
			MD5_Update(&ctx, digest, MD5_DIGEST_LENGTH);

		if (i % 3)
			MD5_Update(&ctx, salt + 3, salt_len);

		if (i % 7)
			MD5_Update(&ctx, password, password_len);

		if (i & 1)
			MD5_Update(&ctx, digest, MD5_DIGEST_LENGTH);
		else
			MD5_Update(&ctx, password, password_len);

		MD5_Final(digest, &ctx);
	}

	cp = result;
	*cp++ = '$'; *cp++ = '1'; *cp++ = '$';
	memcpy(cp, salt + 3, salt_len);
	cp += salt_len;
	*cp++ = '$';

	{
		unsigned long v;
		v = ((unsigned long)digest[ 0] << 16) | ((unsigned long)digest[ 6] << 8) | (unsigned long)digest[12];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 1] << 16) | ((unsigned long)digest[ 7] << 8) | (unsigned long)digest[13];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 2] << 16) | ((unsigned long)digest[ 8] << 8) | (unsigned long)digest[14];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 3] << 16) | ((unsigned long)digest[ 9] << 8) | (unsigned long)digest[15];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 4] << 16) | ((unsigned long)digest[10] << 8) | (unsigned long)digest[ 5];
		to64(cp, v, 4); cp += 4;
		v = (unsigned long)digest[11];
		to64(cp, v, 2); cp += 2;
	}
	*cp = '\0';

	return result;
}

/*
 * SHA-512-crypt implementation.
 * Compatible with crypt(3) $6$ format used by "openssl passwd -6".
 */
static char *
crypt_sha512(const char *password, const char *salt)
{
	SHA512_CTX ctx, ctx1;
	unsigned char digest[SHA512_DIGEST_LENGTH];
	unsigned char alt_result[SHA512_DIGEST_LENGTH];
	static char result[128];
	int password_len = strlen(password);
	int salt_len, i, pl;
	const char *sp;
	char *cp;
	unsigned int rounds = 5000;

	sp = salt + 3;
	/* Check for rounds=NNNNN$ prefix */
	if (strncmp(sp, "rounds=", 7) == 0) {
		rounds = (unsigned int)strtoul(sp + 7, &cp, 10);
		if (rounds < 1000) rounds = 1000;
		if (rounds > 999999999) rounds = 999999999;
		sp = cp + 1; /* skip the $ after rounds */
	}
	for (salt_len = 0; salt_len < 16 && *sp && *sp != '$'; sp++)
		salt_len++;

	SHA512_Init(&ctx);
	SHA512_Update(&ctx, password, password_len);
	SHA512_Update(&ctx, "$6$", 3);
	SHA512_Update(&ctx, salt + 3, sp - (salt + 3));

	SHA512_Init(&ctx1);
	SHA512_Update(&ctx1, password, password_len);
	SHA512_Update(&ctx1, salt + 3, sp - (salt + 3));
	SHA512_Update(&ctx1, password, password_len);
	SHA512_Final(alt_result, &ctx1);

	for (pl = password_len; pl > SHA512_DIGEST_LENGTH;
	     pl -= SHA512_DIGEST_LENGTH)
		SHA512_Update(&ctx, alt_result, SHA512_DIGEST_LENGTH);
	SHA512_Update(&ctx, alt_result, pl);

	memset(alt_result, 0, SHA512_DIGEST_LENGTH);

	for (i = password_len; i != 0; i >>= 1) {
		if (i & 1)
			SHA512_Update(&ctx, alt_result, 1);
		else
			SHA512_Update(&ctx, password, 1);
	}

	SHA512_Final(digest, &ctx);

	for (i = 0; i < (int)rounds; i++) {
		SHA512_Init(&ctx);
		if (i & 1)
			SHA512_Update(&ctx, password, password_len);
		else
			SHA512_Update(&ctx, digest, SHA512_DIGEST_LENGTH);
		if (i % 3)
			SHA512_Update(&ctx, salt + 3, sp - (salt + 3));
		if (i % 7)
			SHA512_Update(&ctx, password, password_len);
		if (i & 1)
			SHA512_Update(&ctx, digest, SHA512_DIGEST_LENGTH);
		else
			SHA512_Update(&ctx, password, password_len);
		SHA512_Final(digest, &ctx);
	}

	cp = result;
	*cp++ = '$'; *cp++ = '6'; *cp++ = '$';
	memcpy(cp, salt + 3, sp - (salt + 3));
	cp += sp - (salt + 3);
	*cp++ = '$';

	{
		unsigned long v;
		v = ((unsigned long)digest[ 0] << 16) | ((unsigned long)digest[21] << 8) | (unsigned long)digest[42];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[22] << 16) | ((unsigned long)digest[43] << 8) | (unsigned long)digest[ 1];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[44] << 16) | ((unsigned long)digest[ 2] << 8) | (unsigned long)digest[23];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 3] << 16) | ((unsigned long)digest[24] << 8) | (unsigned long)digest[45];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[25] << 16) | ((unsigned long)digest[46] << 8) | (unsigned long)digest[ 4];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[47] << 16) | ((unsigned long)digest[ 5] << 8) | (unsigned long)digest[26];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 6] << 16) | ((unsigned long)digest[27] << 8) | (unsigned long)digest[48];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[28] << 16) | ((unsigned long)digest[49] << 8) | (unsigned long)digest[ 7];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[50] << 16) | ((unsigned long)digest[ 8] << 8) | (unsigned long)digest[29];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[ 9] << 16) | ((unsigned long)digest[30] << 8) | (unsigned long)digest[51];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[31] << 16) | ((unsigned long)digest[52] << 8) | (unsigned long)digest[10];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[53] << 16) | ((unsigned long)digest[11] << 8) | (unsigned long)digest[32];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[12] << 16) | ((unsigned long)digest[33] << 8) | (unsigned long)digest[54];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[34] << 16) | ((unsigned long)digest[55] << 8) | (unsigned long)digest[13];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[56] << 16) | ((unsigned long)digest[14] << 8) | (unsigned long)digest[35];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[15] << 16) | ((unsigned long)digest[36] << 8) | (unsigned long)digest[57];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[37] << 16) | ((unsigned long)digest[58] << 8) | (unsigned long)digest[16];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[59] << 16) | ((unsigned long)digest[17] << 8) | (unsigned long)digest[38];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[18] << 16) | ((unsigned long)digest[39] << 8) | (unsigned long)digest[60];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[40] << 16) | ((unsigned long)digest[61] << 8) | (unsigned long)digest[19];
		to64(cp, v, 4); cp += 4;
		v = ((unsigned long)digest[62] << 16) | ((unsigned long)digest[20] << 8) | (unsigned long)digest[41];
		to64(cp, v, 4); cp += 4;
		v = (unsigned long)digest[63];
		to64(cp, v, 2); cp += 2;
	}
	*cp = '\0';

	return result;
}

char *
crypt(const char *password, const char *salt)
{
	char *result;
	if (salt == NULL || password == NULL)
		return NULL;

	if (salt[0] == '$' && salt[2] == '$') {
		if (salt[1] == '1') {
			result = crypt_md5(password, salt);
		} else if (salt[1] == '6') {
			result = crypt_sha512(password, salt);
		} else {
			goto des_fallback;
		}
		if (strcmp(salt, result) == 0)
			return (char *)salt;
		return result;
	}

des_fallback:
	result = DES_crypt(password, salt);
	if (strcmp(salt, result) == 0)
		return (char *)salt;
	return result;
}
